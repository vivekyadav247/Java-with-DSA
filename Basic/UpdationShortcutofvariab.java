public class UpdationShortcutofvariab {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x += 7;
        System.out.println(x);
        x -= 2;
        System.out.println(x);
        x *= 6 ;
        System.out.println(x);
        x /= 12 ;
        System.out.println(x);
    }
}
