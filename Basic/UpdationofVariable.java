public class UpdationofVariable {
    public static void main(String[] args) {
        int x ;
        x = 8 ;
        System.out.println(x);
        x = 10 ;
        System.out.println(x);
        x = x+6 ;
        System.out.println(x);
        x = x-26 ;
        System.out.println(x);
        x = x*-1;
        System.out.println(x);
        x = x/2 ;
        System.out.println(x);

    }
}
